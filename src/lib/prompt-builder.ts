export function buildFinalRecommendationPrompt(
  intentAnalysis: IntentAnalysis,
  language: 'ko' | 'en' | 'jp' | 'cn' | 'vi' | 'mn' | 'uz' | 'ru' | 'th' = 'ko'
): string {
  // 기본 최종 추천 프롬프트
  let prompt = FINAL_RECOMMENDATION_PROMPTS[language];

  // 추천된 치료 정보만 간략하게 추가
  const { recommendedTreatments } = intentAnalysis;

  if (recommendedTreatments && recommendedTreatments.length > 0) {
    // 치료 유형에 따른 니즈-강점 매칭 추가 (한국어만 우선)
    if (language === 'ko') {
      const firstTreatment = recommendedTreatments[0];

      // 치료 유형별 니즈-강점 매칭 선택
      if (firstTreatment === 'nerve_treatment' || recommendedTreatments.includes('nerve_treatment')) {
        prompt += '\n\n' + NEEDS_STRENGTHS_MATCHING.nerve_treatment;
      } else if (firstTreatment === 'wisdom_tooth') {
        prompt += '\n\n' + NEEDS_STRENGTHS_MATCHING.wisdom_tooth;
      } else if (['whitening', 'laminate'].includes(firstTreatment)) {
        prompt += '\n\n' + NEEDS_STRENGTHS_MATCHING.aesthetic;
      } else {
        // 임플란트 및 기타
        prompt += '\n\n' + NEEDS_STRENGTHS_MATCHING.implant;
      }
    }

    prompt += '\n\n## 참고할 치료 정보\n';

    recommendedTreatments.forEach(treatment => {
      switch(treatment) {
        case 'digital_implant':
          prompt += `
### 3D 디지털 가이드 임플란트
- 3D CT + 시뮬레이션으로 정밀 수술
- 수술 시간 30% 단축, 무절개 80% 이상
- 이동현 원장: 통합치의학과 전문의, 조선대 치과병원 외래교수`;
          break;
        case 'all_on_x':
          prompt += `
### 디지털전체임플란트(All on X)
- 이동현 원장: 통합치의학과 전문의, 진단부터 보철까지 원스톱 진료
- 당일 임시치아 장착, 뼈이식 최소화
- 디지털 가이드 시스템으로 정밀하고 안전한 수술`;
          break;
        case 'nerve_treatment':
          prompt += `
### 미세현미경 신경치료
- 이동현 원장: 통합치의학과 전문의
- 25배 미세현미경으로 정밀 치료
- 성공률 90% 이상, 자연치아 최대 보존`;
          break;
        case 'wisdom_tooth':
          prompt += `
### 사랑니 발치
- 3D CT로 신경 위치 정확 파악
- 통증완화시스템(가글마취, 무통마취)으로 편안한 수술
- 최소 절개, 빠른 회복`;
          break;
        case 'whitening':
          prompt += `
### 치아미백
- 식약처 허가 미백제 사용
- 당일 시술 완료`;
          break;
        case 'laminate':
          prompt += `
### 라미네이트
- 최소 삭제로 치아 보존
- 디지털 시스템으로 당일 완성 가능
- 10년 이상 유지`;
          break;
        case 'crown_bridge':
          prompt += `
### 크라운/브릿지
- 디지털 시스템으로 정밀 제작
- 지르코니아: 최고 강도, 15년+ 사용
- CAD/CAM 디지털 정밀 제작`;
          break;
      }
    });
  }


  return prompt;
}

